import './globals.css';
import { Montserrat } from 'next/font/google';
import { cookies } from 'next/headers';
import Image from 'next/image';
import Link from 'next/link';
import type { ReactNode } from 'react';
import { getUser } from '../database/users';
import LoginButton from './(auth)/login/LoginButton';
import LogoutButton from './(auth)/logout/LogoutButton';
import RegisterButton from './(auth)/register/RegisterButton';

type Props = {
  children: ReactNode;
};

type CartItem = {
  id: number;
  name: string;
  printPrice: string;
  imageUrl: string | null;
  quantity: number;
};

export const dynamic = 'force-dynamic';

export const metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

const montserrat = Montserrat({
  subsets: ['latin'],
  weight: ['400', '700'], // Load only required font weights
  variable: '--font-montserrat', // Optional: Use as a CSS variable
  display: 'swap', // Improves performance
});

export default async function RootLayout({ children }: Props) {
  const sessionTokenCookie = (await cookies()).get('sessionToken');
  const user = sessionTokenCookie && (await getUser(sessionTokenCookie.value));

  const cartItemsCookie = (await cookies()).get('cart');

  const cartItems: CartItem[] = !cartItemsCookie
    ? []
    : (JSON.parse(cartItemsCookie.value) as CartItem[]);
  const totalQuantity = cartItems.reduce(
    (acc: number, item: CartItem) => acc + item.quantity,
    0,
  );

  return (
    <html lang="en" data-theme="corporate" className={montserrat.className}>
      <body>
        <header className="sticky top-0 z-50">
          <div>
            <nav className="navbar bg-base-100 shadow-md">
              <Link className="m-2 mr-8" href="/">
                <Image
                  src="/images/logo.svg"
                  alt="Logo"
                  width={100}
                  height={100}
                />
              </Link>
              <div className="navbar-start">
                <Link href="/models" className="m-3">
                  3D Models
                </Link>
                <Link href="/models" className="m-3">
                  How It Works
                </Link>
              </div>
              <div className="navbar-center hidden lg:flex">
                <div className="input">
                  <svg
                    className="h-[1em] opacity-50"
                    xmlns="http://www.w3.org/2000/svg"
                    viewBox="0 0 24 24"
                  >
                    <g
                      strokeLinejoin="round"
                      strokeLinecap="round"
                      strokeWidth="2.5"
                      fill="none"
                      stroke="currentColor"
                    >
                      <circle cx="11" cy="11" r="8" />
                      <path d="m21 21-4.3-4.3" />
                    </g>
                  </svg>
                  <input type="search" required placeholder="Search" />
                </div>
              </div>

              <div className="navbar-end">
                <Link href="/cart" className="m-4">
                  <div className="relative">
                    <Image
                      src="/icons/shopping-cart.svg"
                      alt="cart"
                      width={25}
                      height={25}
                    />
                    {totalQuantity > 0 && (
                      <span className="badge badge-xs badge-info text-xs absolute -top-3.5 -right-4">
                        {totalQuantity}
                      </span>
                    )}
                  </div>
                </Link>

                {user ? (
                  <div className="dropdown dropdown-hover dropdown-end m-2">
                    <Link href={`/profile/${user.userName}`}>
                      <Image
                        src="/icons/user.svg"
                        alt="user"
                        width={25}
                        height={25}
                      />
                    </Link>
                    <ul className="dropdown-content menu bg-base-100 rounded-box z-1 w-52 p-2 shadow-sm">
                      <li className="font-semibold pb-1">{`logged in as ${user.userName}`}</li>
                      <li>
                        <a href={`/profile/${user.userName}`}>My Account</a>
                      </li>
                      <li>
                        <a href={`/profile/${user.userName}/wishlist`}>
                          My Wishlist
                        </a>
                      </li>
                    </ul>
                  </div>
                ) : (
                  <div className="dropdown dropdown-hover m-2">
                    <Link href="/login">
                      <Image
                        src="/icons/user.svg"
                        alt="user"
                        width={25}
                        height={25}
                      />
                    </Link>
                    <ul className="dropdown-content menu bg-base-100 rounded-box z-1 w-52 p-2 shadow-sm">
                      <li>
                        <a href="/register">Create account</a>
                      </li>
                      <li>
                        <a href="/login">Login</a>
                      </li>
                    </ul>
                  </div>
                )}

                {user ? (
                  <LogoutButton />
                ) : (
                  <div>
                    <Link href="/register">
                      <RegisterButton />
                    </Link>
                    <Link href="/login">
                      <LoginButton />
                    </Link>
                  </div>
                )}
              </div>
            </nav>
          </div>
        </header>
        <main>{children}</main>
        <footer className="footer sm:footer-horizontal bg-neutral text-neutral-content p-10 mt-auto">
          LayerForge Â®
          <div>
            <h4>Services</h4>
            <a className="link link-hover">Branding</a>
            <a className="link link-hover">Design</a>
            <a className="link link-hover">Marketing</a>
            <a className="link link-hover">Advertisement</a>
          </div>
          <div>
            <h4>Company</h4>
            <a className="link link-hover">About us</a>
            <a className="link link-hover">Contact</a>
            <a className="link link-hover">Jobs</a>
            <a className="link link-hover">Press kit</a>
          </div>
          <div>
            <h4>Legal</h4>
            <a className="link link-hover">Terms of use</a>
            <a className="link link-hover">Privacy policy</a>
            <a className="link link-hover">Cookie policy</a>
          </div>
        </footer>
      </body>
    </html>
  );
}
